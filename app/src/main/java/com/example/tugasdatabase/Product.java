package com.example.tugasdatabase;

public class Product {

    private int id;
    private String judul;
    private int rak;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public int getRak() {
        return rak;
    }

    public void setRak(int rak) {
        this.rak = rak;
    }


}
